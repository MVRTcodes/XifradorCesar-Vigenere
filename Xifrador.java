
public abstract class Xifrador {
	public Xifrador() {
		
	}
	
	public String xifrar(String frase) {
		return frase;
	}
	
	public String xifrar(String frase, String clau) {
		return frase;
	}
	
	public String desxifrar(String frase) {
		return frase;
	}
	
	public String desxifrar(String frase, String clau) {
		return frase;
	}

	
}
